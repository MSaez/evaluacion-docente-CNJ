		return array(
		);
	}

	/**
	 * @return array customized attribute labels (name=>label)
	 */
	public function attributeLabels()
	{
		return array(
			'id' => 'ID',
			'username' => 'Rut',
			'password' => 'Contraseña',
			'ev_nombres' => 'Nombres',
			'ev_paterno' => 'Apellido Paterno',
